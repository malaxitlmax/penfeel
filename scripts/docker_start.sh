#!/bin/bash

# Скрипт для сборки и запуска всего приложения через Docker

# Переходим в корневую директорию проекта
cd "$(dirname "$0")/.."

echo "Запуск сборки и запуска через Docker..."

# Собираем и запускаем контейнеры
docker compose up --build -d

# Проверяем, что контейнеры запустились
echo "Проверка запущенных контейнеров:"
docker compose ps

echo "Приложение доступно по адресу: http://localhost:8080"
echo "Для просмотра логов используйте: docker compose logs -f"
echo "Для остановки используйте: docker compose down" 